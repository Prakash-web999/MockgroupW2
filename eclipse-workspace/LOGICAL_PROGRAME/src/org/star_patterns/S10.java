package org.star_patterns;

public class S10 {

	public static void main(String[] args) 
	{

		for (int i = 1; i <= 4; i++)
		{
	        for (int j = 0; j < i; j++)
	        {
	            System.out.print("*");
	        }
	        System.out.println();
	}
	System.out.println("-------------");
           
	        int Star=1;
           
		for (int i = 1; i <= 4; i++)
		{
	        for (int j = 0; j < Star; j++)
	        {
	            System.out.print("*");
	        }
	        System.out.println();
	        Star++;
		}
	
	
	
	
	}
	
	
	
}
